    private void jBGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGravarActionPerformed
        // TODO add your handling code here:
        RegistroVeiculos tempVeiculo = new RegistroVeiculos(jTFMarca.getText(),jTFModelo.getText(), jTFPlaca.getText(), jTFAno.getText(), jComboBox1.getSelectedItem().toString());
        if(codigo ==-1){
            meusVeiculos.insereVeiculos(tempVeiculo);
        } else meusVeiculos.atualizarVeiculos(tempVeiculo, this.codigo);
        this.dispose();
    }//GEN-LAST:event_jBGravarActionPerformed

