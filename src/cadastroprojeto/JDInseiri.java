        public void carregarDados(){
            RegistroVeiculos auxVeiculo=meusVeiculos.getVeiculos(this.codigo);
            jTFMarca.setText(auxVeiculo.getMarca());
            jTFModelo.setText(auxVeiculo.getMarca());
            jTFPlaca.setText(auxVeiculo.getPlaca());
            jTFAno.setText(auxVeiculo.getAno());
            jComboBox1.setSelectedItem(auxVeiculo.getCorId(auxVeiculo.getidCor()));
        }
    
      

